/**
 * Faça um programa que peça dois números inteiros e 
 * imprima a soma desses dois números
 * @author Paulo Fegueredo
**/

public class Ex01_Soma {

	public static void main(String[] args) {
		soma(5, 7);

	}
	
	public static void soma(int num1, int num2) {
		System.out.println(num1 + num2);;
	}

}
